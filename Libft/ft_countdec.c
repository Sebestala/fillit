#include "libft.h"

int	ft_countdec(int n)
{
	long	l;
	int		count;

	l = n;
	count = 0;
	if (l < 0)
	{
		count++;
		l = -l;
	}
	while (l > 9)
	{
		count++;
		l /= 10;
	}
	count++;
	return (count);
}
